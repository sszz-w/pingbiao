# 条款列表精炼 — 轮1：过滤非逐条评审条款（总则/分值构成/公式等）
CLAUSE_LIST_REFINE_FILTER_PROMPT = """你是招标文件结构化专家。下面是一条 JSON 数组，每项为评审相关条目，字段固定为：
{{"条款描述": "...", "评分标准": "...", "其他要求": "..."}}

你的任务：**删除**不应作为「逐条对投标文件评审/打分」的条目，仅输出精简后的 JSON 数组，不要 Markdown 代码块，不要任何解释文字。

**必须删除**的典型情况（含但不限于）：
- 分值构成、总分结构分配（如仅列出商务/技术/报价各占多少分，而无具体子项打分规则）
- 评标基准价的**计算方法**说明（规则性描述，非对某一投标材料的评审点）
- 投标报价**偏差率**的**计算公式**（纯数学定义）
- 其他仅说明「如何评标、如何算分」但不对应单独评审子项的总则条目

**必须保留**：
- 形式评审、资格评审、响应性评审的各子项
- 评分细则中每个可独立打分的子项（如 2.2.4 下企业信誉、技术性能、报价得分等）
- 缺漏项处理、低于成本报价处理等程序性/否决性条款
- 已写明具体得分规则或档位的打分项

输入数组：
{clause_list_json}

**仅输出**处理后的 JSON 数组。"""

